    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        irManager = (ConsumerIrManager) getSystemService(Context.CONSUMER_IR_SERVICE);

        Button powerBtn = findViewById(R.id.powerBtn);
        Button foundBtn = findViewById(R.id.foundBtn);
        TextView statusText = findViewById(R.id.statusText);
        TextView codeText = findViewById(R.id.codeText);

        if (irManager == null || !irManager.hasIrEmitter()) {
            statusText.setText("ERROR: No IR blaster found on this device!");
            powerBtn.setEnabled(false);
            return;
        }

        statusText.setText("Ready. Press POWER and point at TV.");

        powerBtn.setOnClickListener(v -> {
            if (currentIndex >= patterns.length) {
                statusText.setText("All codes tried. None worked.");
                return;
            }

            irManager.transmit(frequencies[currentIndex], patterns[currentIndex]);

            statusText.setText("Attempt #" + (currentIndex + 1) + " of " + patterns.length);
            codeText.setText("Frequency: " + frequencies[currentIndex] + " Hz\nCode index: " + currentIndex);

            currentIndex++;
        });

        foundBtn.setOnClickListener(v -> {
            int workedIndex = currentIndex - 1;
            statusText.setText("SUCCESS! Code #" + workedIndex + " worked!");
            codeText.setText(
                "Frequency: " + frequencies[workedIndex] + " Hz\n" +
                "Code index: " + workedIndex + "\n\n" +
                "Screenshot this and send to Claude!"
            );
            found = true;
        });
    }
